function getRgbColor( c )
{
    var colorHash = {
        "無":"#DCAA6B",    // 段ボール色
        "白":"#ffffff",
        "肌":"#ffcccc",
        "茶":"#800000",
        "赤":"#ff0000",
        "黄":"#ffff00",
        "緑":"#00ff00",
        "水":"#00ffff",
        "青":"#0000ff",
        "紫":"#800080"
    };
    return colorHash[ c ];
}

var stats;

gr(function(){
    function init() {
        stats = new Stats();
        stats.setMode( 0 ); // 0: fps, 1: ms, 2: mb
        stats.domElement.style.position = "fixed";
        stats.domElement.style.left     = "5px";
        stats.domElement.style.top      = "5px";
        document.body.appendChild(stats.domElement);

        for ( var i = 0; i < dataSet.length; i++ ) {
            var x = i % 16;
            var y = Math.floor(i/16);
            var px = x - 8;
            var pz = y - 8;
            var position = 'position="' + px + ',0,' + pz + '"';
            var color = getRgbColor(dataSet[i]);
            gr("#canvas")("scene").append('<mesh geometry="cube" scale="0.4, 0.4, 0.4" ' + position + ' color="'+color+'"/>');
            
        }
    }
    
    var ratio = 0;
    function rot() {
        gr('#canvas')('mesh').setAttribute('rotation', ratio + ',' + ratio + ',' + ratio);
        ratio += 2;
        stats.update();
        requestAnimationFrame(rot);
    }
    
    init();
    rot();
});