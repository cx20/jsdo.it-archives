function getColorByMag( mag )
{
    var color = 0x000000;
    if ( mag <= 0 ) {
        color = 0xaaaaaa;  // 灰色
    } else if ( mag > 0 && mag <= 1 ) {
        color = 0xffff00;  // 黄色
    } else if ( mag > 1 && mag <= 2 ) {
        color = 0xffa020; // オレンジ
    } else if ( mag > 2 ) {
        color = 0xff0000; // 赤
    }
    return color;
}

