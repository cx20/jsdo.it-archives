var pos = 0;
function render() {

    var particles = dataSet.length;
    if ( pos > particles ) {
        pos = 0; // 時間軸の位置
    }
    var sizes = geometry.attributes.size.array;
    var near = 0; // 時間軸からの近さ
    var mag = 0; // マグニチュード
    for (var i = 0; i < particles; i++) {
        sizes[i] = 0;
        near = Math.abs(i - pos);
        mag = dataSet[i].mag;
        // 時間軸に近いほどサイズを強調させる
        if (near < 5) {
            sizes[i] = mag * 30;
        } else if (near < 10) {
            sizes[i] = mag * 20;
        } else if (near < 50) {
            sizes[i] = mag * 10;
        } else if (near < 100) {
            sizes[i] = mag * 5;
        }
    }
    geometry.attributes.size.needsUpdate = true;
    var element = document.getElementById("time");
    element.innerHTML = dataSet[pos].date;
    pos += 5;
    
    renderer.render( scene, camera );

}
