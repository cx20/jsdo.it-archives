function plot(scene, dataSet) {
    uniforms = {
        color:     { type: "c", value: new THREE.Color( 0xffffff ) },
        texture:   { type: "t", value: new THREE.TextureLoader().load( "../../assets/y/V/x/3/yVx3R.png" ) } // ball.png
        //texture:   { type: "t", value: new THREE.TextureLoader().load( "ball.png" ) }
    };


    var shaderMaterial = new THREE.ShaderMaterial( {
        uniforms:       uniforms,
        vertexShader:   document.getElementById( 'vertexshader' ).textContent,
        fragmentShader: document.getElementById( 'fragmentshader' ).textContent,
        blending:       THREE.AdditiveBlending,
        depthTest:      false,
        transparent:    true
    });

    geometry = new THREE.BufferGeometry();
    var particles = dataSet.length;
    var positions = new Float32Array( particles * 3 );
    var colors = new Float32Array( particles * 3 );
    var sizes = new Float32Array( particles );
    var color = new THREE.Color();
    for ( var i = 0, i3 = 0; i < particles; i ++, i3 += 3 ) {
        var data = dataSet[i];
        // 表示範囲
        // 経度：129.5600～132.3480
        // 緯度：31.7100～34.0500

        // 経度：140.4200～142.8000
        // 緯度：42.0600～43.8260
        
        //var x = ((data.long - 130.9540)/2.788)*800;
        //var z = (-(data.lat - 32.8800)/2.34)*800;

        //var x = ((data.long - 140)/2.788)*800;
        //var z = (-(data.lat - 42)/2.34)*800;

        var x = ((data.long - 141.61)/2.38)*800;
        var z = (-(data.lat - 42.943)/1.766)*800;

        var y = -(data.dep*5);
        var w = (data.mag) * 5;
        positions[ i3 + 0 ] = x;
        positions[ i3 + 1 ] = y;
        positions[ i3 + 2 ] = z;

        // HSL の色相は 0～60度が赤～黄色の範囲である為、
        // 震度に応じて黄色～赤となるよう調整
        // ＜参考＞
        // ■ 色相 - Wikipedia
        // https://ja.wikipedia.org/wiki/%E8%89%B2%E7%9B%B8
        //
        color.setHSL((1-(data.mag/7))*60/360, 1.0, 0.5 );

        colors[ i3 + 0 ] = color.r;
        colors[ i3 + 1 ] = color.g;
        colors[ i3 + 2 ] = color.b;
        sizes[ i ] = w;
    }
    geometry.addAttribute( 'position', new THREE.BufferAttribute( positions, 3 ) );
    geometry.addAttribute( 'customColor', new THREE.BufferAttribute( colors, 3 ) );
    geometry.addAttribute( 'size', new THREE.BufferAttribute( sizes, 1 ) );
    particleSystem = new THREE.Points( geometry, shaderMaterial );
    scene.add( particleSystem );
}

