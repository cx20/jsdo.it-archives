function getRgbColor( c )
{
	var colorHash = {
		"無":"#000000",
		"白":"#ffffff",
		"肌":"#ffcccc",
		"茶":"#800000",
		"赤":"#ff0000",
		"黄":"#ffff00",
		"緑":"#00ff00",
		"水":"#00ffff",
		"青":"#0000ff",
		"紫":"#800080"
	};
	return colorHash[ c ];
}

var DOT_SIZE = 12;
var X_START_POS = 50;
var Y_START_POS = 50;

function init()
{
	var paper = Raphael(0, 0, 465, 465);
	
	var i, x, y;
	for ( i = 0; i < dataSet.length; i++ ) {
		x = ( i % 16 ) * DOT_SIZE + X_START_POS;
		y = ( Math.floor( i / 16 ) * DOT_SIZE + Y_START_POS );
		paper.rect( x, y, 10, 10 ).attr("fill",  getRgbColor( dataSet[i] ) );
	}
}
