var Point  = Isomer.Point;
var Path   = Isomer.Path;
var Shape  = Isomer.Shape;
var Color  = Isomer.Color;

var c0  = new Color( 0x00, 0x00, 0x00 ); // 無
var c1  = new Color( 0xff, 0xff, 0xff ); // 白
var c2  = new Color( 0xff, 0xcc, 0xcc ); // 肌
var c3  = new Color( 0x80, 0x00, 0x00 ); // 茶
var c4  = new Color( 0xff, 0x00, 0x00 ); // 赤
var c5  = new Color( 0xff, 0xff, 0x00 ); // 黄
var c6  = new Color( 0x00, 0xff, 0x00 ); // 緑
var c7  = new Color( 0x00, 0xff, 0xff ); // 水
var c8  = new Color( 0x00, 0x00, 0xff ); // 青
var c9  = new Color( 0x80, 0x00, 0x80 ); // 紫

var colorHash = {
    "無": c0, // 0x000000,
    "白": c1, // 0xffffff,
    "肌": c2, // 0xffcccc,
    "茶": c3, // 0x800000,
    "赤": c4, // 0xff0000,
    "黄": c5, // 0xffff00,
    "緑": c6, // 0x00ff00,
    "水": c7, // 0x00ffff,
    "青": c8, // 0x0000ff,
    "紫": c9  // 0x800080
};

function getRgbColor( c )
{
    return colorHash[ c ];
}

var iso;
var angle = 0;
var pos = 0;

function init() {
    iso = new Isomer(document.getElementById("canvas"));
    draw();
}

function draw() {
    iso.canvas.clear();
    for (var j = 0; j < dataSet.length; j++) {
        for (var i = dataSet[j].length - 1; i >= 0 ; i--) {
            x = (((i+pos) % 16)) * DOT_SIZE + X_START_POS;
            y = (15-j) * DOT_SIZE + Y_START_POS;
            z = (15-Math.floor(i / 16)) * DOT_SIZE + Z_START_POS;
            c = dataSet[j][i];

            if (dataSet[j][i] != "無") {
                var color = getRgbColor( dataSet[j][i] );
                iso.add(Shape.Prism(
                        new Point(x, y, z),
                        1 * DOT_SIZE * 0.9,
                        1 * DOT_SIZE * 0.9,
                        1 * DOT_SIZE * 0.9),
                    color);
            }
        }
    }
    pos++;
    requestAnimationFrame( draw );
}

init();
