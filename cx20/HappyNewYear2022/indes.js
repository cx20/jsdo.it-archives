const canvas = document.getElementById("c");
const ctx = canvas.getContext("2d");
drawInit();
let progress = 0;
let intervalId1 = setInterval(drawProgress, 10);
let intervalId2 = 0;
let intervalId3 = 0;

function drawInit() {
    for (let i = 0; i < dataSet.length; i++) {
        let data = dataSet[i];
        ctx.fillStyle = "rgb(235, 237, 240)";
        let x = data.x;
        let y = data.y;
        let width = 11;
        let height = 11;
        ctx.fillRect(x, y, width, height);
    }
}

function drawProgress() {
    if (progress >= dataSet.length) {
        clearInterval(intervalId1);
        progress = 0;
        intervalId2 = setInterval(drawGreeting, 10);
        return;
    }
    let data = dataSet[progress];
    ctx.fillStyle = colorSet[data.level-1];
    let x = data.x;
    let y = data.y;
    let width = 11;
    let height = 11;
    ctx.fillRect(x, y, width, height);
    progress++;
}

function drawGreeting() {
    if (progress >= dataSet.length) {
        progress = 0;
        clearInterval(intervalId2);
        intervalId3 = setInterval(clearBackground, 10);
        return;
    }
    let data = dataSet[progress];
    if (data.color != "") {
        ctx.fillStyle = data.color;
        let x = data.x;
        let y = data.y;
        let width = 11;
        let height = 11;
        ctx.fillRect(x, y, width, height);
    }
    progress++;
}

function clearBackground() {
    if (progress >= dataSet.length) {
        progress = 0;
        clearInterval(intervalId3);
        return;
    }
    let data = dataSet[progress];
    if (data.color != "") {
        ctx.fillStyle = "rgb(128, 128, 128)";
        let x = data.x;
        let y = data.y;
        let width = 11;
        let height = 11;
        ctx.fillRect(x, y, width, height);
    }
    progress++;
}
