        let y1 = y / 10 + Math.random() * 0.01;
        let z1 = -2 + Math.random() * 0.01;
        let i = x + (15-y) * 16;
        let color = getRgbColor( dataSet[i] );
        
        /*
            <a-sphere position="0 2 -2" dynamic-body  radius="0.5" material="src: #football-texture"></a-sphere>
        */
